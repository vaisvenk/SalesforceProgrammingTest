
public class Coordinate  
{
	private double x;
	private double y;
	
	public Coordinate(double y, double x)
	{
		setX(x);
		setY(y);
	}
	
	public double getX()
	{
		return x;
	}
	public double getY()
	{
		return y;
	}
	public void setX(double x)
	{
		this.x = x;
	}
	public void setY(double y)
	{
		this.y = y;
	}
	

}
